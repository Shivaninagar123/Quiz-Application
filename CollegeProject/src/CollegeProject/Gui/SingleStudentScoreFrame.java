    private void cbUserIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbUserIdActionPerformed
        try{
            ArrayList<String> examid=PerformanceDao.getExamIdByUserId(cbUserId.getSelectedItem().toString());
            cbExamId.removeAllItems();
            for(String id:examid){
                cbExamId.addItem(id);
            }
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"DB ERROR","SingleStudentScore!!",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }

    }//GEN-LAST:event_cbUserIdActionPerformed

