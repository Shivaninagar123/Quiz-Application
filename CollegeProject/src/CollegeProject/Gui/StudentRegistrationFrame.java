private boolean isValidate() {
        char[] psw=passwordField1.getPassword();
        char[] ps=passwordField2.getPassword();
    if(txtUserId.getText().isEmpty()||psw.length==0||ps.length==0)  
        return false;
    return true;
    }
    private boolean matchReTypePassword(){
        String ps1 = String.valueOf(passwordField1.getPassword());
        String ps2 =String.valueOf(passwordField2.getPassword());
        if(ps1.equals(ps2))
            return true;
        return false;
    }
