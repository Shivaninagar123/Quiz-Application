 private void loadData() {
      try{
          ArrayList<String> userId=PerformanceDao.getAllUserId();
           if(userId.size()==0){
               JOptionPane.showMessageDialog(null,"No Student Take Exam Yet","DB error!!",JOptionPane.ERROR_MESSAGE);
               return;
           }
          for(String id:userId){
           cbUserId.addItem(id);
         }
          
      }
       catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"DB ERROR","SingleStudentScore!!",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
}


