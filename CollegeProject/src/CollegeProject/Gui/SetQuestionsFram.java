    private void jbtnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnNextActionPerformed
        if(validateInput()==false){
            JOptionPane.showMessageDialog(null,"Please fill all the field");
            return;
            
        }
        String optionName=options.get(correctOption);
        Question obj= new Question(newExam.getExamId(),newExam.getLanguage(),qno,question,option1,option2,option3,option4,optionName);
        qstore.addQuestion(obj);
        clearAll();
        jlblTitle.setText(newExam.getTotalQuestions()-qno+"Questions Remaining");
        qno++;
        if(qno>newExam.getTotalQuestions()){
            disableAll();
            JOptionPane.showMessageDialog(null,"Your questions have all been preapred Successfully \n Press the done button to addthem to the database","Exam added",JOptionPane.INFORMATION_MESSAGE);
           System.out.println(qstore);
        }
        else{
            jlblQno.setText(jlblQno.getText()+qno);
        }
        
    }//GEN-LAST:event_jbtnNextActionPerformed

    private void jbtnDoneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnDoneActionPerformed
        if(qno<=newExam.getTotalQuestions()){
           int x= newExam.getTotalQuestions()-qno+1;
           JOptionPane.showMessageDialog(null,"You still have"+x+"Questions remaining ","fill all the questions ",JOptionPane.ERROR_MESSAGE);
           return;
       }
       try{
           boolean done= ExamDao.addExam(newExam);
           if(!done){
               JOptionPane.showMessageDialog(null,"Exam not added in Db","try again",JOptionPane.ERROR_MESSAGE);
               return;
               
           
       }
           QuestionDao.addQuestions(qstore);
           JOptionPane.showMessageDialog(null," Yours questions successfully added to the database","Exam added",JOptionPane.INFORMATION_MESSAGE);
           AdminOptionsFrame adminframe=new AdminOptionsFrame();
           adminframe.setVisible(true);
           this.dispose();
           
       }
       catch(SQLException ex){
           JOptionPane.showMessageDialog(null,"DB Error","Set Questions Error!",JOptionPane.ERROR_MESSAGE);
           
       }
    }//GEN-LAST:event_jbtnDoneActionPerformed
     
