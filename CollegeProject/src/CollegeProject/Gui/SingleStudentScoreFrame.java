    private void cbExamIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbExamIdActionPerformed
        if(cbExamId.getItemCount()==0)
        return;
        try{
            StudentScore result=PerformanceDao.getScore(cbUserId.getSelectedItem().toString(), cbExamId.getSelectedItem().toString());
            txtSubject.setText(result.getSubject());
            txtPercentage.setText(result.getPercentage()+"");
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"DB ERROR","SingleStudentScore!!",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_cbExamIdActionPerformed

