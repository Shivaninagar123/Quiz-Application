private boolean validateInput(){
    question=jtxtQuestion.getText().trim();
    option1=jtxtQuestion1.getText().trim();
     option2=jtxtQuestion2.getText().trim();
      option3=jtxtQuestion3.getText().trim();
       option4=jtxtQuestion4.getText().trim();
       correctOption=jcCorrectOption.getSelectedItem().toString();
       if(question.isEmpty()||option1.isEmpty()||option2.isEmpty()||option3.isEmpty()||option4.isEmpty()||correctOption.isEmpty()){
           return false;
       }
      return true;
    
}













