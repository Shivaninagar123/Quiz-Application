    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        if(!isValidate())
        {
            JOptionPane.showMessageDialog(null,"Please fill all Details First ", "Invalid",JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(!matchReTypePassword()){
            JOptionPane.showMessageDialog(null,"Password Mismatch Try Again..", "Invalid",JOptionPane.ERROR_MESSAGE);
            return;
        }
        String userId=txtUserId.getText();
        String password=String.valueOf(passwordField1.getPassword());//array type password convert to string type
        User user=new User(userId,password,"Student");
        try {

            boolean r=UserDao.isRegistered(userId,"Student");
            if(r){
                JOptionPane.showMessageDialog(null,"Already Student Registired With this Userid", "Registratio Fail!!",JOptionPane.ERROR_MESSAGE);
                return;
            }
            boolean b= UserDao.addUser(user);
            if(!b){
                JOptionPane.showMessageDialog(null," Student Registired Successfull", "Registration",JOptionPane.INFORMATION_MESSAGE);
                txtUserId.setText("");
                passwordField1.setText("");
                passwordField2.setText("");
                return;
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"DB ERROR","Student registration frame!!",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }

    }//GEN-LAST:event_btnRegisterActionPerformed

